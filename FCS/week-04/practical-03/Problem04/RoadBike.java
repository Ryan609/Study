public class RoadBike extends Bicycle {

	private String tyres;

	//No-parameter construction
	public RoadBike() {}
	//Constructor with parameters
	public RoadBike(String color, int year, int numberGear,boolean is_serviced, String inDate, 
						String outDate, String serviceResponsible, String tyres) {

		super(color,year,numberGear,is_serviced,inDate,outDate,serviceResponsible);
		this.tyres = tyres;
	}

	//get and set 
	public String getTyres() { return tyres; }
	public void setTyres(String tyres) { this.tyres = tyres; }

}