public class Node {
	private Node next;
	private Student info;

	public Node() {
		this.next = null;
	}

	public Node(Student tmpStudent) {
		this.info = tmpStudent;
		this.next = null;
	}

	public void setNext(Node next) {
		this.next = next;
	}

	public void setInfo(Student info) {
		this.info = info;
	}

	public Node getNext() {
		return this.next;
	}

	public Student getInfo() {
		return this.info;
	}
}