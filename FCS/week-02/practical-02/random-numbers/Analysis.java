import java.util.Random;

public class Analysis{
    public static void main(String[] args) {
         
        //Create an object.
        Random number = new Random();
        //Define the initial value of sum to be 0
        int sum = 0;

        //Use loop to get 10 random numbers within 20
     	for(int i = 1; i < 11; i++) {
     		int num = number.nextInt(20);
            //Print out the random number.
     		System.out.println("Number" + i + " is: " + num);
     		sum += num;
        }
            //Define the average value as a float type.
     	    float mean = (float)sum/10;
            //Print out the sum and average of numbers
     	    System.out.println("sum = " + sum);
     	    System.out.println("mean = " + mean);   
     }
}